import matplotlib.pyplot as plt
x = []
y = []
for line in open("DATA.dat", "r"):
	import matplotlib.pyplot as plt
	values = [float(s) for s in line.split()]
	x.append(values[2])
	y.append(values[0])

plt.xlabel("Total running time")
plt.ylabel("Kinetic energy cut off")
a = plt.plot(x ,y)
plt.show(a)

